    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // consultamos mediante la funcion getText() el valor que el usuario
        // ingreso en el campo
        String username = txtUser.getText();
        String password = txtPassword.getText();
        
        System.out.println("username: "+ username + " password: "+ password);
        // 2. Validad si alguno de los dos campos esta vacio
        if (username.isEmpty() ||  password.isEmpty()){
            JOptionPane.showMessageDialog(this,"Hay campos sin diligenciar.", "Login", JOptionPane.WARNING_MESSAGE);
        }else{
            // validar el escenario en el que la informacion corresponde a los datos de inicio de sesion 
            if(username.equals("MisionTIC" )&& password.equals("Ciclo2")){
                JOptionPane.showMessageDialog(this, "Bienvenido al sistema");
                //4. crear una instancia de la vista UserMenu
                UserMenu userMenu = new UserMenu();
                //5 hacer visible la vista mediante el metodo set visible 
                userMenu.setVisible(true); 
                // 6. eliminar la vista del Login
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Error. Revisa los valores suministrados", "login", JOptionPane.ERROR_MESSAGE);
            }
        
        }
                
    }//GEN-LAST:event_btnLoginActionPerformed

